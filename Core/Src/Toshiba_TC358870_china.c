void SendDSIcfg(void)
{
	TC358870_i2c_Write(0x0500,0x0004, 2); // CMD_SEL
	TC358870_i2c_Write(0x0110,0x00000016, 4); // MODE_CONFIG
	TC358870_i2c_Write(0x0310,0x00000016, 4); // MODE_CONFIG

	/* ILI7807D DCS initialization sequence via TC358870 DCSCMD_Q (0x0504) */

	/* SLPOUT - Sleep Out (0x11), no parameter */
	TC358870_i2c_Write(0x0504, 0x0005, 2); // DCS Short Write, no param
	TC358870_i2c_Write(0x0504, 0x0011, 2); // command: SLPOUT
	HAL_Delay(120);

	/* COLMOD - Set Pixel Format (0x3A) = 0x77 (24-bit RGB) */
	TC358870_i2c_Write(0x0504, 0x0015, 2); // DCS Short Write, 1 param
	TC358870_i2c_Write(0x0504, 0x773A, 2); // cmd=0x3A, param=0x77
	HAL_Delay(10);

	/* NORON - Normal Display Mode On (0x13), no parameter */
	TC358870_i2c_Write(0x0504, 0x0005, 2); // DCS Short Write, no param
	TC358870_i2c_Write(0x0504, 0x0013, 2); // command: NORON
	HAL_Delay(10);

	/* INVOFF - Display Inversion Off (0x20), no parameter */
	TC358870_i2c_Write(0x0504, 0x0005, 2); // DCS Short Write, no param
	TC358870_i2c_Write(0x0504, 0x0020, 2); // command: INVOFF
	HAL_Delay(10);

	/* TEON - Tearing Effect Line On (0x35), VSYNC mode (param=0x00) */
	TC358870_i2c_Write(0x0504, 0x0015, 2); // DCS Short Write, 1 param
	TC358870_i2c_Write(0x0504, 0x0035, 2); // cmd=0x35, param=0x00
	HAL_Delay(10);

	/* DISPON - Display On (0x29), no parameter */
	TC358870_i2c_Write(0x0504, 0x0005, 2); // DCS Short Write, no param
	TC358870_i2c_Write(0x0504, 0x0029, 2); // command: DISPON
	HAL_Delay(10);

	/* Switch back to video mode after command transmission */
	TC358870_i2c_Write(0x0110,0x00000006, 4); // MODE_CONFIG
	TC358870_i2c_Write(0x0310,0x00000006, 4); // MODE_CONFIG

	printf("ILI7807D init sequence complete.\r\n");
}

