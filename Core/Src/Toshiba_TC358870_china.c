void Toshiba_EnableMIPI(void)
{
	printf("\r\n--- Attempting to enable MIPI PLL and lanes ---\r\n");

	/* Basic sequence mirrored from Setup_DSITX but with extra delays and checks */
	TC358870_i2c_Write(0x0108,0x00000001, 4); // DSI_TX_CLKEN
	TC358870_i2c_Write(0x010C,0x00000001, 4); // DSI_TX_CLKSEL

	/* TX0 PLL sequence */
	TC358870_i2c_Write(0x02A0,0x00000001, 4); // MIPI_PLL_CONTROL
	TC358870_i2c_Write(0x02AC,0x000090CF, 4); // MIPI_PLL_CNF (use existing value)
	HAL_Delay(5);
	TC358870_i2c_Write(0x02A0,0x00000003, 4); // MIPI_PLL_CONTROL start
	HAL_Delay(10);

	/* Enable lanes (try default used in Setup_DSITX first) */
	TC358870_i2c_Write(0x0118,0x00000014, 4); // LANE_ENABLE (first attempt)
	HAL_Delay(10);

	uint32_t v32 = 0;
	if (TC358870_i2c_Read32(0x0118, &v32) == I2C_RESULT_SUCCESS) {
		printf("After write, LANE_ENABLE(0x0118)=0x%08lX\r\n", (unsigned long)v32);
	} else {
		printf("Failed to read back LANE_ENABLE(0x0118) after first attempt\r\n");
	}

	/* If still zero, try a more aggressive enable (common 4-lane mask) */
	if (v32 == 0) {
		printf("LANE_ENABLE still 0 -> trying alternate value 0x1F\r\n");
		TC358870_i2c_Write(0x0118,0x0000001F, 4);
		HAL_Delay(10);
		if (TC358870_i2c_Read32(0x0118, &v32) == I2C_RESULT_SUCCESS) {
			printf("After alternate write, LANE_ENABLE(0x0118)=0x%08lX\r\n", (unsigned long)v32);
		}
	}

	/* Also configure TX1 PLL/lane similarly */
	TC358870_i2c_Write(0x0308,0x00000001, 4); // DSI_TX_CLKEN
	TC358870_i2c_Write(0x030C,0x00000001, 4); // DSI_TX_CLKSEL
	TC358870_i2c_Write(0x04A0,0x00000001, 4); // MIPI_PLL_CONTROL (TX1)
	TC358870_i2c_Write(0x04AC,0x000090CF, 4); // MIPI_PLL_CNF (TX1)
	HAL_Delay(5);
	TC358870_i2c_Write(0x04A0,0x00000003, 4);
	HAL_Delay(10);
	TC358870_i2c_Write(0x0318,0x00000014, 4);
	HAL_Delay(10);

	/* Read back key status registers */
	uint16_t clk = TC358870_i2c_Read16(0x0006);
	printf("CLK_STATUS (0x0006) after enabling MIPI = 0x%04X\r\n", clk);
	uint16_t sys = TC358870_i2c_Read16(0x0004);
	printf("SYS_STATUS (0x0004) after enabling MIPI = 0x%04X\r\n", sys);

	printf("--- End Attempt to enable MIPI ---\r\n\r\n");
}

void Toshiba_ResetPanelAndRetry(void)
{
	printf("\r\n--- Reset/Power-cycle panel and retry MIPI enable ---\r\n");

	/* Try a safe sequence: toggle panel reset pins A/B/C/D and power rails E10/E4/E6 */
	HAL_GPIO_WritePin(E10_GPIO_Port, E10_Pin, GPIO_PIN_RESET); /* VSP/VSN off */
	HAL_Delay(10);

	/* Toggle panel resets low */
	HAL_GPIO_WritePin(A_GPIO_Port, A_Pin, GPIO_PIN_RESET);
	HAL_GPIO_WritePin(B_GPIO_Port, B_Pin, GPIO_PIN_RESET);
	HAL_GPIO_WritePin(C_GPIO_Port, C_Pin, GPIO_PIN_RESET);
	HAL_GPIO_WritePin(D_GPIO_Port, D_Pin, GPIO_PIN_RESET);
	HAL_Delay(20);

	/* Ensure power rails are asserted: E4 (1.8V), E6 (3.3V) */
	HAL_GPIO_WritePin(E4_GPIO_Port, E4_Pin, GPIO_PIN_SET);
	HAL_GPIO_WritePin(E6_GPIO_Port, E6_Pin, GPIO_PIN_SET);
	HAL_Delay(20);

	/* Release reset lines */
	HAL_GPIO_WritePin(A_GPIO_Port, A_Pin, GPIO_PIN_SET);
	HAL_GPIO_WritePin(B_GPIO_Port, B_Pin, GPIO_PIN_SET);
	HAL_GPIO_WritePin(C_GPIO_Port, C_Pin, GPIO_PIN_SET);
	HAL_GPIO_WritePin(D_GPIO_Port, D_Pin, GPIO_PIN_SET);
	HAL_Delay(50);

	/* Turn VSP/VSN back on */
	HAL_GPIO_WritePin(E10_GPIO_Port, E10_Pin, GPIO_PIN_SET);
	HAL_Delay(20);

	/* Try enabling MIPI again */
	Toshiba_EnableMIPI();

	/* Dump registers to report result */
	Toshiba_DumpRegisters();

	printf("--- End reset-and-retry ---\r\n\r\n");
}
